
public class Employee {
	

	
	String firstName;
	String lastName;
	int age;
	double monthlySalary;
	
	String getFirstName() {
		return firstName;
	}
	
	String getLastName() {
		return lastName;
	}
	
	int getAge() {
		return age;
	}
		
	double getMonthlySalary() {
		return monthlySalary;
	}
	
}

